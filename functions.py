def area(width, height):
    result = width * height
    return result

result = area(5, 6)
result2 = area(6, 4)
result3 = area(8, 9)

print(result)
print(result2)
print(result3)
print("\n")


def subtract(num1, num2):
    subtract_result = num1 - num2
    return subtract_result

subtract_result_1 = subtract(20, 5)
subtract_result_2 = subtract(15, 10)

print(subtract_result_1)
print(subtract_result_2)
print("\n")

